	@SuppressWarnings("unchecked")
	static class TaskX {

		public void solve(int testNumber, InputReader in, PrintWriter out) {

			int n = in.nextInt(), m = in.nextInt();
			List<P>[] g = new ArrayList[n];
			g = Stream.generate(ArrayList::new).limit(n).toArray(List[]::new);

			for (int i = 0; i < m; i++) {
				int p = in.nextInt()-1, y = in.nextInt();
				g[p].add(new P(i, p, y));
			}

			for (int i = 0; i < n; i++) {
				Collections.sort(g[i]);
			}

			String[] ans = new String[m];
			for (int i = 0; i < n; i++) {
				for (int j = 0; j < g[i].size(); j++) {
					String ret = zp(String.valueOf(g[i].get(j).p+1), 6) + zp(String.valueOf(j+1), 6);
					ans[g[i].get(j).idx] = ret;
				}
			}

			for (String str : ans) {
				out.println(str);
			}

		}
		String zp(String str, int len) {
			return String.format("%" + len + "s", str).replace(" ", "0");
		}
	}


	static class P implements Comparable<P> {
		int idx;
		int p;
		int y;


		public P(int idx, int p, int y) {
			super();
			this.idx = idx;
			this.p = p;
			this.y = y;
		}


		@Override
		public String toString() {
			return "P [idx=" + idx + ", p=" + p + ", y=" + y + "]";
		}


		@Override
		public int compareTo(P o) {
			return Integer.compare(this.y, o.y);
		}

	}

	static class InputReader {
		BufferedReader in;
		StringTokenizer tok;

		public String nextString() {
			while (!tok.hasMoreTokens()) {
				try {
					tok = new StringTokenizer(in.readLine(), " ");
				} catch (IOException e) {
					throw new InputMismatchException();
				}
			}
			return tok.nextToken();
		}

		public int nextInt() {
			return Integer.parseInt(nextString());
		}

		public long nextLong() {
			return Long.parseLong(nextString());
		}

		public double nextDouble() {
			return Double.parseDouble(nextString());
		}

		public int[] nextIntArray(int n) {
			int[] res = new int[n];
			for (int i = 0; i < n; i++) {
				res[i] = nextInt();
			}
			return res;
		}

		public int[] nextIntArrayDec(int n) {
			int[] res = new int[n];
			for (int i = 0; i < n; i++) {
				res[i] = nextInt() - 1;
			}
			return res;
		}

		public int[] nextIntArray1Index(int n) {
			int[] res = new int[n + 1];
			for (int i = 0; i < n; i++) {
				res[i + 1] = nextInt();
			}
			return res;
		}

		public long[] nextLongArray(int n) {
			long[] res = new long[n];
			for (int i = 0; i < n; i++) {
				res[i] = nextLong();
			}
			return res;
		}

		public long[] nextLongArrayDec(int n) {
			long[] res = new long[n];
			for (int i = 0; i < n; i++) {
				res[i] = nextLong() - 1;
			}
			return res;
		}

		public long[] nextLongArray1Index(int n) {
			long[] res = new long[n + 1];
			for (int i = 0; i < n; i++) {
				res[i + 1] = nextLong();
			}
			return res;
		}

		public double[] nextDoubleArray(int n) {
			double[] res = new double[n];
			for (int i = 0; i < n; i++) {
				res[i] = nextDouble();
			}
			return res;
		}

		public InputReader(InputStream inputStream) {
			in = new BufferedReader(new InputStreamReader(inputStream));
			tok = new StringTokenizer("");
		}
	}

