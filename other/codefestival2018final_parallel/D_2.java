	static class TaskX {

		public void solve(int testNumber, MyInput in, PrintWriter out) {

			int N = in.nextInt();
			char[][] strs = new char[N][];
			for (int i = 0; i < N; i++) {
				strs[i] = in.nextChars();
			}

			int[][][] cnt = new int[52][52][52];
			boolean[][][] used = new boolean[52][52][52];
			int max = -1;

			for (char[] str : strs) {
				int m = str.length;
				int[] L = new int[52], R = new int[52];
				fill(used, false);

				for (char c : str) R[c2i(c)]++;

				for (int i = 0; i < m; i++) {
					int n = c2i(str[i]);
					R[n]--;
					for (int left = 0; left < 52; left++) {
						for (int right = 0; right < 52; right++) {
							if (L[left] >= 1 && R[right] >= 1 && !used[left][n][right]) {
								max = Math.max(max, ++cnt[left][n][right]);
								used[left][n][right] = true;
							}
						}
					}
					L[n]++;
				}
			}

			out.println(max);

			for (int i = 0; i < 52; i++) {
				for (int j = 0; j < 52; j++) {
					for (int k = 0; k < 52; k++) {
						if (max == cnt[i][j][k]) {
							String ans = String.valueOf(i2c(i)) + String.valueOf(i2c(j)) + String.valueOf(i2c(k));
							out.println(ans);
							return;
						}
					}
				}
			}

		}

		int c2i(char c) {
			int ret = -1;
			if ('A' <= c && c <= 'Z') {
				ret = c - 'A';
			} else if ('a' <= c && c <= 'z') {
				ret = c - 'a' + 26;
			}
			return ret;
		}

		char i2c(int i) {
			char ret = '*';
			if (0 <= i && i < 26) {
				ret = (char)(i + 'A');
			} else {
				ret = (char)(i - 26 + 'a');
			}
			return ret;
		}

		void fill(boolean[][][] used, boolean state) {
			for (int i = 0; i < used.length; i++) {
				for (int j = 0; j < used[0].length; j++) {
					for (int k = 0; k < used[0][0].length; k++) {
						used[i][j][k] = state;
					}
				}
			}
		}
	}

	static class MyInput {
		private final BufferedReader in;
		private static int pos;
		private static int readLen;
		private static final char[] buffer = new char[1024 * 8];
		private static char[] str = new char[500 * 8 * 2];
		private static boolean[] isDigit = new boolean[256];
		private static boolean[] isSpace = new boolean[256];
		private static boolean[] isLineSep = new boolean[256];

		static {
			for (int i = 0; i < 10; i++) {
				isDigit['0' + i] = true;
			}
			isDigit['-'] = true;
			isSpace[' '] = isSpace['\r'] = isSpace['\n'] = isSpace['\t'] = true;
			isLineSep['\r'] = isLineSep['\n'] = true;
		}

		public MyInput(InputStream is) {
			in = new BufferedReader(new InputStreamReader(is));
		}

		public int read() {
			if (pos >= readLen) {
				pos = 0;
				try {
					readLen = in.read(buffer);
				} catch (IOException e) {
					throw new RuntimeException();
				}
				if (readLen <= 0) {
					throw new MyInput.EndOfFileRuntimeException();
				}
			}
			return buffer[pos++];
		}

		public int nextInt() {
			int len = 0;
			str[len++] = nextChar();
			len = reads(len, isSpace);
			int i = 0;
			int ret = 0;
			if (str[0] == '-') {
				i = 1;
			}
			for (; i < len; i++)
				ret = ret * 10 + str[i] - '0';
			if (str[0] == '-') {
				ret = -ret;
			}
			return ret;
		}

		public long nextLong() {
			int len = 0;
			str[len++] = nextChar();
			len = reads(len, isSpace);
			int i = 0;
			long ret = 0;
			if (str[0] == '-') {
				i = 1;
			}
			for (; i < len; i++)
				ret = ret * 10 + str[i] - '0';
			if (str[0] == '-') {
				ret = -ret;
			}
			return ret;
		}

		public char nextChar() {
			while (true) {
				final int c = read();
				if (!isSpace[c]) {
					return (char) c;
				}
			}
		}

		public String nextString() {
			return new String(nextChars());
		}

		public char[] nextChars() {
			int len = 0;
			str[len++] = nextChar();
			len = reads(len, isSpace);
			return Arrays.copyOf(str, len);
		}

		public char[][] next2DChars(int h, int w) {
			char[][] s = new char[h][w];
			for (int i = 0; i < h; i++) {
				s[i] = nextChars();
			}
			return s;
		}

		int reads(int len, boolean[] accept) {
			try {
				while (true) {
					final int c = read();
					if (accept[c]) {
						break;
					}
					if (str.length == len) {
						char[] rep = new char[str.length * 3 / 2];
						System.arraycopy(str, 0, rep, 0, str.length);
						str = rep;
					}
					str[len++] = (char) c;
				}
			} catch (MyInput.EndOfFileRuntimeException e) {
			}
			return len;
		}

		public int[] nextIntArray(final int n) {
			final int[] res = new int[n];
			for (int i = 0; i < n; i++) {
				res[i] = nextInt();
			}
			return res;
		}

		public int[] nextIntArray1Index(final int n) {
			final int[] res = new int[n + 1];
			for (int i = 1; i < n + 1; i++) {
				res[i] = nextInt();
			}
			return res;
		}

		public int[] nextIntArrayDec(final int n) {
			final int[] res = new int[n];
			for (int i = 0; i < n; i++) {
				res[i] = nextInt() - 1;
			}
			return res;
		}

		public long[] nextLongArray(final int n) {
			final long[] res = new long[n];
			for (int i = 0; i < n; i++) {
				res[i] = nextLong();
			}
			return res;
		}

		public long[] nextLongArray1Index(final int n) {
			final long[] res = new long[n + 1];
			for (int i = 1; i < n + 1; i++) {
				res[i] = nextLong();
			}
			return res;
		}

		public long[] nextLongArrayDec(final int n) {
			final long[] res = new long[n];
			for (int i = 0; i < n; i++) {
				res[i] = nextLong() - 1;
			}
			return res;
		}

		public double nextDouble() {
			return Double.parseDouble(nextString());
		}

		public double[] nextDoubleArray(int n) {
			double[] res = new double[n];
			for (int i = 0; i < n; i++) {
				res[i] = nextDouble();
			}
			return res;
		}

		static class EndOfFileRuntimeException extends RuntimeException {
		}

	}

